// Level Construction
export const DEFAULT_LEVEL_MAP: number[][] = [];
const LEVEL_WIDTH = 220;
const LEVEL_HEIGHT = 15;

for (let y = 0; y < LEVEL_HEIGHT; y++) {
  DEFAULT_LEVEL_MAP.push(Array(LEVEL_WIDTH).fill(TileType.AIR));
}

const set = (x: number, y: number, t: TileType) => {
  if (x >= 0 && x < LEVEL_WIDTH && y >= 0 && y < LEVEL_HEIGHT) DEFAULT_LEVEL_MAP[y][x] = t;
};
const fillRect = (x: number, y: number, w: number, h: number, t: TileType) => {
  for(let iy=0; iy<h; iy++) for(let ix=0; ix<w; ix++) set(x+ix, y+iy, t);
};
const placePipe = (x: number, h: number) => {
  fillRect(x, 13-h, 2, h, TileType.PIPE_L); 
  set(x, 13-h, TileType.PIPE_TOP_L);
  set(x+1, 13-h, TileType.PIPE_TOP_R);
  for(let i=1; i<=h; i++) {
    set(x, 13-h+i, TileType.PIPE_L);
    set(x+1, 13-h+i, TileType.PIPE_R);
  }
};
const placeGround = (start: number, width: number) => {
  fillRect(start, 13, width, 2, TileType.GROUND);
};

